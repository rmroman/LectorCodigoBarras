public class MainActivity extends AppCompatActivity
{
    private int CODIGO_SCANNER = 600;
    private TextView tvCodigo;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        tvCodigo = findViewById(R.id.tvCodigo);
    }


    public void escanear(View v) {
        Intent intScanner = new Intent(this, ScannerCodigo.class);
        startActivityForResult(intScanner, CODIGO_SCANNER);
    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, @Nullable Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (requestCode==CODIGO_SCANNER) {
            if (resultCode==RESULT_OK) {
                tvCodigo.setText(data.getData().toString());
            }
        }
    }
}
